// Helper function to get all packages as flat array
export const getAllPackages = () => {
    const allPackages = [];

    // Get East Java packages
    if (packagesData.EAST_JAVA) {
        Object.keys(packagesData.EAST_JAVA).forEach(duration => {
            if (Array.isArray(packagesData.EAST_JAVA[duration])) {
                allPackages.push(...packagesData.EAST_JAVA[duration]);
            }
        });
    }

    // Get Rinjani packages
    if (packagesData.RINJANI) {
        Object.keys(packagesData.RINJANI).forEach(duration => {
            if (Array.isArray(packagesData.RINJANI[duration])) {
                allPackages.push(...packagesData.RINJANI[duration]);
            }
        });
    }

    return allPackages;
};

export const getPackageById = (id) => {
    const allPackages = getAllPackages();
    return allPackages.find(pkg => pkg.id === id);
}

export const getPackagesByBadge = (badge) => {
    const allPackages = getAllPackages();
    return allPackages.filter(pkg => pkg.badge === badge);
}

export const getFeaturedPackages = (limit = 6) => {
    const allPackages = getAllPackages();
    return allPackages.slice(0, limit);
}

export const getPackagesByDestination = (destination) => {
    if (destination === 'EAST_JAVA' && packagesData.EAST_JAVA) {
        const packages = [];
        Object.keys(packagesData.EAST_JAVA).forEach(duration => {
            if (Array.isArray(packagesData.EAST_JAVA[duration])) {
                packages.push(...packagesData.EAST_JAVA[duration]);
            }
        });
        return packages;
    }

    if (destination === 'RINJANI' && packagesData.RINJANI) {
        const packages = [];
        Object.keys(packagesData.RINJANI).forEach(duration => {
            if (Array.isArray(packagesData.RINJANI[duration])) {
                packages.push(...packagesData.RINJANI[duration]);
            }
        });
        return packages;
    }

    return [];
}

export const getPackagesByDuration = (duration) => {
    const allPackages = getAllPackages();
    return allPackages.filter(pkg => pkg.durasiHari === duration);
}

export const getNewPackages = (limit = 6) => {
    const allPackages = getAllPackages();
    let newPackages = allPackages.filter(pkg => pkg.hasBadge && pkg.badge === "NEW");
    
    // If not enough NEW packages, add other packages to fill the limit
    if (newPackages.length < limit) {
        const otherPackages = allPackages.filter(pkg => !newPackages.includes(pkg));
        newPackages = [...newPackages, ...otherPackages.slice(0, limit - newPackages.length)];
    }
    
    return newPackages.slice(0, limit);
}